#creating a list 
my_list = [1,2,3,4,5]
print("Original List:",my_list)

#Accessing elements
print("first elemnt:",my_list[0])
print("last element:",my_list[-1])

my_list.append(6)
print("new list:",my_list)

my_list.remove(6)
print("list is :",my_list)

my_list.sort()
print("sorted list is :",my_list)

my_list.reverse()
print("reversed list is :",my_list)